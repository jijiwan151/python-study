# 문자열은 보통 따옴표("" 또는 '') 로 감싸서 표현해.
이름 = "아들"
인사 = "안녕!"

print(이름)  # 출력: 아들
print(인사)  # 출력: 안녕!

# 2. 문자열 연결하기 (+)
# 문자열을 합쳐서 문장을 만들 수 있어.

이름 = "철수"
인사 = "안녕, " + 이름 + "!"
print(인사)  # 출력: 안녕, 철수!

# 3. 문자열 반복하기 (*)
# 같은 문장을 여러 번 반복할 수도 있어.
응원 = "화이팅! " * 3
print(응원)  # 출력: 화이팅! 화이팅! 화이팅!

# 4. 문자열 길이 구하기 (len())
# 문자열의 글자 수(길이)를 알고 싶을 때 사용해.
문장 = "파이썬 재미있다!"
print(len(문장))  # 출력: 10

# 5. 문자열 인덱싱 (위치 찾기)
# 문자열에서 특정 글자만 뽑을 수도 있어. 문자는 0부터 번호(인덱스)를 가진다.
이름 = "파이썬"

print(이름[0])  # 출력: 파
print(이름[1])  # 출력: 이
print(이름[-1]) # 출력: 썬 (뒤에서 첫 번째)

# 6. 문자열 자르기 (슬라이싱)
# 문자열의 일부만 가져올 수도 있어.
문장 = "안녕하세요, 철수입니다."

print(문장[0:5])  # 출력: 안녕하세요
print(문장[7:])   # 출력: 철수입니다.

# 7. 대소문자 변환 (upper(), lower())
텍스트 = "Hello Python"

print(텍스트.upper())  # 출력: HELLO PYTHON
print(텍스트.lower())  # 출력: hello python

# 8. 실전 예제 - 자기소개 만들기
# 아들에게 자신의 이름과 나이를 입력받아 문장을 만드는 프로그램을 보여줘.
이름 = input("이름을 입력하세요: ")
나이 = input("나이를 입력하세요: ")

문장 = "안녕하세요! 저는 " + 이름 + "이고, " + 나이 + "살입니다."
print(문장)
