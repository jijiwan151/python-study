
# 변수란
용돈 = 5000  # 5000원을 용돈 변수에 저장
print(용돈)  # 출력: 5000

# # 숫자 (정수, 실수)
# 정수(int) : 1, 2, 100, -5
# 실수 (float) → 3.14, 0.5, -2.75 같은 소수점이 있는 숫자

사과_개수 = 3      # 정수 (int)
사과_가격 = 2.5    # 실수 (float)

총_가격 = 사과_개수 * 사과_가격
print(총_가격)  # 출력: 7.5

# 기본 연산
a = 10
b = 3

print(a + b)  # 덧셈: 13
print(a - b)  # 뺄셈: 7
print(a * b)  # 곱셈: 30
print(a / b)  # 나눗셈: 3.3333...
print(a // b) # 몫: 3
print(a % b)  # 나머지: 1
print(a ** b) # 거듭제곱: 1000 (10^3)

