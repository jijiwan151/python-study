# 5. 튜플 길이 구하기 (len())
과일 = ("사과", "바나나", "포도")
print(len(과일))  # 출력: 3

# 6. 튜플 언패킹 (여러 변수에 한 번에 저장)
# 튜플은 여러 개의 변수를 한 번에 저장하는 데 유용해.
좌표 = (10, 20)  # (x, y) 좌표

x, y = 좌표  # 튜플 언패킹
print(x)  # 출력: 10
print(y)  # 출력: 20

# 7. 실전 예제 - 요일 표시
# 튜플을 사용해서 요일을 쉽게 가져오는 예제
요일 = ("월", "화", "수", "목", "금", "토", "일")

오늘_요일 = 요일[3]  # 인덱스 3 → 목요일
print("오늘은", 오늘_요일, "요일입니다.")
# 출력: 오늘은 목요일입니다.
