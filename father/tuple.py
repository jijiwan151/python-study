# 1. 튜플이란?
# 튜플은 여러 개의 값을 저장하는 고정된 바구니야.
# 📌 리스트([])와 달리 튜플은 ()로 묶어 사용해.

과일리스트 = ["사과", "바나나", "포도"]
과일 = ("사과", "바나나", "포도")
print(과일)  # 출력: ('사과', '바나나', '포도')

# 3. 튜플의 요소 접근 (인덱싱)
# 튜플도 리스트처럼 인덱스를 사용해 값을 가져올 수 있어.
과일 = ("사과", "바나나", "포도")

print(과일[0])  # 출력: 사과 (첫 번째 요소)
print(과일[1])  # 출력: 바나나 (두 번째 요소)
print(과일[-1]) # 출력: 포도 (마지막 요소)

