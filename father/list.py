# 1. 리스트란?
# 리스트는 여러 개의 값을 한꺼번에 저장하는 바구니 같은 거야.
# 대괄호 [] 안에 값을 넣어서 만들 수 있어.

과일 = ["사과", "바나나", "포도"]
print(과일)  # 출력: ['사과', '바나나', '포도']


# 2. 리스트의 요소 가져오기 (인덱싱)
# 리스트에서 특정 위치의 값을 가져올 수 있어.
# (숫자는 0번부터 시작!)

과일 = ["사과", "바나나", "포도"]

print(과일[0])  # 출력: 사과 (첫 번째)
print(과일[1])  # 출력: 바나나 (두 번째)
print(과일[2])  # 출력: 포도 (세 번째)
print(과일[-1]) # 출력: 포도 (마지막 요소)

# 3. 리스트 요소 변경하기
# 리스트의 특정 요소를 바꿀 수도 있어.

과일 = ["사과", "바나나", "포도"]
과일[1] = "딸기"  # 바나나 → 딸기로 변경

print(과일)  # 출력: ['사과', '딸기', '포도']

# 4. 리스트 길이 구하기 (len())
과일 = ["사과", "바나나", "포도"]
print(len(과일))  # 출력: 3

# 5. 리스트에 요소 추가하기 (append())
과일 = ["사과", "바나나"]
과일.append("오렌지")  # 리스트에 추가
과일.append("레몬")  # 리스트에 추가
print(과일)  # 출력: ['사과', '바나나', '오렌지']

# 6. 리스트에서 요소 삭제하기 (remove(), pop())
과일 = ["사과", "바나나", "포도"]

과일.remove("바나나")  # '바나나' 삭제
print(과일)  # 출력: ['사과', '포도']

삭제된_과일 = 과일.pop()  # 마지막 요소 삭제
print(과일)  # 출력: ['사과']
print(삭제된_과일)  # 출력: 포도

# 7. 리스트 정렬하기 (sort(), reverse())
숫자 = [3, 1, 4, 2]

숫자.sort()  # 오름차순 정렬
print(숫자)  # 출력: [1, 2, 3, 4]

숫자.reverse()  # 뒤집기
print(숫자)  # 출력: [4, 3, 2, 1]

# 8. 리스트 반복문 (for)
과일 = ["사과", "바나나", "포도"]

for f in 과일:
    print(f)

# 9. 실전 예제 - 쇼핑 리스트
쇼핑리스트 = []

# 3개의 항목 입력받기
for i in range(3):
    아이템 = input("쇼핑할 물건을 입력하세요: ")
    쇼핑리스트.append(아이템)

print("쇼핑 리스트:", 쇼핑리스트)



